    public boolean addDelivery(String billNo, String custName, String telNo, String address, String date, String driver, String state) {
        
        try {
            
            String sql = "INSERT INTO `delivery`(`salesNo`, `custName`, `custmobileNo`, `custAddress`, `deliveryDateTime`, `availableDriver`, `deliveryState`)"
                    + "VALUES('"+billNo+"', '"+custName+"', '"+telNo+"', '"+address+"', '"+date+"', '"+driver+"',  '"+state+"')";
            
            stmt = con.prepareStatement(sql);
            stmt.execute(sql);
            
            return true;
            
        } catch (SQLException ex) {
            Logger.getLogger(DeliveryUI.class.getName()).log(Level.SEVERE, null, ex);
        }
                
        return false;
        
    }
        
    
