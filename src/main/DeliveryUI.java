    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        String billNo = txtBillno.getText();
        String custName = txtCustName.getText();
        String telNo = txtcustTelNo.getText();
        String address = txtcustAddress.getText();
        
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        
        int driverChoice = jComboBoxdrivers.getSelectedIndex();
        int stateChoice = jComboBoxState.getSelectedIndex();
        
        String state = null;
        String driver = null;
        
        
        
        
        int size = jComboBoxdrivers.getItemCount();
        
        for (int i = 0; i < size; i++) {
            
             if(driverChoice == i) 
                 driver = (jComboBoxdrivers.getSelectedItem().toString());
        }
        
        if(stateChoice == 0) 
            state = "Pending";
        else if (stateChoice == 1)
            state = "Delivered";
        
        if(addDelivery(billNo, custName, telNo, address, date, driver, state)) {
            JOptionPane.showMessageDialog(null,"Successfully Inserted");
            
//            txtCustName.setText("");
//            txtcustTelNo.setText("");
//            txtcustTelNo.setText("");
//            txtcustAddress.setText("");
            jComboBoxdrivers.setSelectedIndex(0);
            jComboBoxState.setSelectedIndex(0);
            
        } else {
            JOptionPane.showMessageDialog(null, "Please Enter All Fields");
            txtCustName.requestFocus();
        }
        
        
    }//GEN-LAST:event_addBtnActionPerformed

