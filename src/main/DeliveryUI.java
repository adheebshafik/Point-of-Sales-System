    private void getRowNumber() {
        try {
            
            stmt = con.createStatement();
            rs = stmt.executeQuery("SELECT * FROM sales ORDER BY salesId DESC LIMIT 1;");
            // extract data from the ResultSet
            if (rs.next()) {
                
            int id = rs.getInt(1); 
            id++;
            txtBillno.setText(Integer.toString(id));
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(DeliveryUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void showallDrivers() {
        
        try {
            
            stmt = con.createStatement();
            String sql = "select DISTINCT fName from employee where post = 'Rider'";
            
            rs = stmt.executeQuery(sql);
            
            while(rs.next()) {
                jComboBoxdrivers.addItem(rs.getString("fName"));
            }
            
        } catch (Exception e) {
        }
        
        
    }
    

