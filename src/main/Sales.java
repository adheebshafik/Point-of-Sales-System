    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtPay.getText().equalsIgnoreCase("")==false ){
        
        float pay = Float.parseFloat(txtPay.getText());
        float subTotal = Float.parseFloat(txtSubTotal.getText());
            
        float balance = pay - subTotal;
            
        txtBalance.setText(String.valueOf(balance));
            
            
        if (Float.parseFloat(txtPay.getText()) < Float.parseFloat(txtSubTotal.getText())) {
        
            JOptionPane.showMessageDialog(null, "Cash Received lesser than SubTotal.");
            
        } else {
        
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to print ?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            print();
        }
            
        sales();
            
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
            
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        txtSubTotal.setText("");
        txtPay.setText("");
        txtBalance.setText("");
        
        txtProdCode.requestFocus();
        
        getRowNumber();
        
        }
        
        }   else if (jTable1.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Cart is Empty.");
                txtProdCode.requestFocus();
        }   else{
                JOptionPane.showMessageDialog(null, "Enter the Amount Received.");
                txtPay.requestFocus();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

