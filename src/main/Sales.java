    private void sales() {
        
//        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("MM/dd/yyyy");
//        LocalDateTime now = LocalDateTime.now();
            

//        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("MM/dd/yyyy");
//        LocalDateTime now = LocalDateTime.now();
//        String date = dtf.format(now);


//        DateFormat formatter = new SimpleDateFormat("MM/dd/yyyy");
//        LocalDate now = LocalDate.now();
//        Date myDate = format.parse(date);

        //yyyy-MM-dd
        
        
        java.sql.Date sqlDate = new java.sql.Date(new java.util.Date().getTime());

       
        
        String subTotal = txtSubTotal.getText();
        String pay = txtPay.getText();
        String balance = txtBalance.getText();
        int lastinsertid = 0;
        
        String sql = "insert into sales(date, subTotal, pay, balance) values (?,?,?,?)"; 
        
        try {
            
            
            pst = con.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
            pst.setDate(1, sqlDate);
            pst.setString(2, subTotal);
            pst.setString(3, pay);
            pst.setString(4, balance);
            pst.executeUpdate();
            
            rs  = pst.getGeneratedKeys();
            
            if (rs.next()) {
                
                lastinsertid = rs.getInt(1);
            
            }
            
            int rows = jTable1.getRowCount();
            
            String sql2 = "insert into sales_product (salesId, productId, sellPrice, qty, total)"
                          + "values (?,?,?,?,?)";
            
            pst = con.prepareStatement(sql2);
            
            String product_id = "";
            String price = "";
            String qty = "";
            int tot = 0;
            
            
            for (int i = 0; i < rows; i++) {
                
                product_id = (String) jTable1.getValueAt(i, 0);
                price = (String) jTable1.getValueAt(i, 3);
                qty = (String) jTable1.getValueAt(i, 4);
                tot = (int) jTable1.getValueAt(i, 5);
                
                pst.setInt(1, lastinsertid);
                pst.setString(2, product_id);
                pst.setString(3, price);
                pst.setString(4, qty);
                pst.setFloat(5, tot);
                pst.executeUpdate();
                
            }
            
            
            String sql3 = "update product SET quantity = quantity - ? where barcode = ?";
            pst = con.prepareStatement(sql3);
            
            
            for (int i = 0; i < rows; i++) {
                
                product_id = (String) jTable1.getValueAt(i, 0);
                qty = (String) jTable1.getValueAt(i, 4);
                
                pst.setString(1, qty);
                pst.setString(2, product_id);
                pst.execute();
                
            }
            
            pst.addBatch();
            JOptionPane.showMessageDialog(this, "Record Added");
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
