    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Connection con = DBconnect.connect();

            JasperDesign jasdi = JRXmlLoader.load("C:\\Users\\Mohamed Ayyash\\Desktop\\POS\\src\\main\\CustomerReport.jrxml");
            //            String sql = "SELECT `cardNo`, `fName`, `lName`, `address`, `membershipType`, `discount` FROM `customer` WHERE `membershipType` = \"Gold\"";
            String sql = "SELECT `cardNo`, `fName`, `lName`, `address`, `membershipType`, `discount` FROM `customer` WHERE `membershipType` = 'Platinum'";
            JRDesignQuery newQuery = new JRDesignQuery();
            newQuery.setText(sql);
            jasdi.setQuery(newQuery);
            JasperReport js = JasperCompileManager.compileReport(jasdi);
            JasperPrint jp  = JasperFillManager.fillReport(js, null, con);

            JasperViewer.viewReport(jp, false);

        } catch (JRException ex) {
            JOptionPane.showMessageDialog(jButton1, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

